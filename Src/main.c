/**
  * @brief  Frame Event callback.
  * @param  hdcmi: pointer to a DCMI_HandleTypeDef structure that contains
  *                the configuration information for DCMI.
  * @retval None
  */
void HAL_DCMI_FrameEventCallback(DCMI_HandleTypeDef *hdcmi)
{
  /* NOTE : This function Should not be modified, when the callback is needed,
            the HAL_DCMI_FrameEventCallback could be implemented in the user file
   */
	
    uint8_t *p;
		uint32_t i=0,jpgstart=0,jpglen=0; 
		uint8_t  head=0;

		
    HAL_DCMI_Stop(hdcmi);
		HAL_GPIO_TogglePin(LD4_GPIO_Port, LD4_Pin);
	
		p=(uint8_t*)&CAMERA_FRAME_BUFFER;
		for(i=0;i<1024*32; i++) //search for 0XFF 0XD8 and 0XFF 0XD9, get size of JPG 
		{
						if((p[i]==0XFF)&&(p[i+1]==0XD8))
						{
										jpgstart=i;
										head=1;	// Already found  FF D8
						}
						if((p[i]==0XFF)&&(p[i+1]==0XD9)&&head)  //search for FF D9
						{
										jpglen=i-jpgstart+2;
										break;
						}
		}
		if(jpglen)	 
		{
			HAL_UART_Transmit(&huart1, p, jpglen, 2000);
		}   
	
	
		HAL_DCMI_Start_DMA(hdcmi, DCMI_MODE_CONTINUOUS, (uint32_t)(&CAMERA_FRAME_BUFFER), 32 * 1024 / 4);
		
		__HAL_DCMI_ENABLE_IT(hdcmi,DCMI_IT_FRAME); 
	
}

